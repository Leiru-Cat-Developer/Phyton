'''
    HACER UN PROGRAMA QUE PIDA UN CARACTER E INDIQUE SI ES UNA VOCAL O NO
'''
caracter = input("DIGITE UN CARACTER: ").lower()    #CONVERTIR A MINUSCULA

if caracter == 'a' or caracter == 'A':
    print("LA LETRA ES UNA VOCAL")
elif caracter == 'e' or caracter == 'E':
    print("LA LETRA ES UNA VOCAL")
elif caracter == 'i' or caracter == 'I':
    print("LA LETRA ES UNA VOCAL")
elif caracter == 'o' or caracter == 'O':
    print("LA LETRA ES UNA VOCAL")
elif caracter == 'u' or caracter == 'U':
    print("LA LETRA ES UNA VOCAL")
else:
    print("LA LETRA NO ES UNA VOCAL")